// Helper function to get icon with custom size/class
export function getIcon(name, { size = 16, class: className = '' } = {}) {
    const icon = icons[name];
    if (!icon) {
        console.warn(`Icon "${name}" not found`);
        return '';
    }
    return icon.replace('<svg', `<svg width="${size}" height="${size}" class="${className}"`);
}

